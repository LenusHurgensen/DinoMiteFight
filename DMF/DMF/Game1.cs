        /// <param name="gameTime">Provides a snapshot of timing values.</param>
        protected override void Update(GameTime gameTime)
        {
            if (Keyboard.GetState().IsKeyDown(Keys.F))
            {
                graphics.IsFullScreen = !graphics.IsFullScreen;
                graphics.ApplyChanges();
            }

            // TODO: Add your update logic here
            player.Update(gameTime);
            player2.Update(gameTime);
            camera.Update(new Vector2(map.Width / 2, map.Height / 2));
            foreach (CollisionTiles tile in map.CollsionTiles)
            {
                player.Collision(tile.Rectangle, map.Width, map.Height);
                player2.Collision(tile.Rectangle, map.Width, map.Height);
            }

            if (player2.Position.X > player.Position.X + map.Width / 2)
                camera.Zoom -= 0.01f;
            else if (player.Position.X > player2.Position.X + map.Width / 2)
                camera.Zoom -= 0.01f;
            else camera.Zoom += 0.01f;

            base.Update(gameTime);
        }

